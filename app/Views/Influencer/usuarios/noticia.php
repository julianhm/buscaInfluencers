                        <p style="text-align:center"><span style="color:#e74c3c">En el nuevo mundo digital, la publicidad encontr&oacute; nuevas formas y canales de comunicaci&oacute;n para llegar a sus audiencias de manera m&aacute;s precisa y efectiva, aprovechando otras formas de hablar y generar contenido con la irrupci&oacute;n de otras figuras que ahora son las nuevas celebridades. Para lograr este objetivo, el alcance org&aacute;nico es la clave.&nbsp;</span></p>


                        <p style="text-align:center">Este alcance es la audiencia, las personas que son potenciales compradores de la marca. Esta audiencia puede aumentar <span style="color:#e74c3c">incluyendo en la nueva campa&ntilde;a de marketing de tu marca, idea o emprendimiento</span>, a uno o varios influencers.&nbsp;&nbsp;</p>

                        <p style="text-align:center">Como la palabra lo dice, un influencer es aquella persona que tiene la facilidad de influir. Esta es la capacidad de tener un efecto sobre el car&aacute;cter, opini&oacute;n, tema, inter&eacute;s, gusto, desarrollo o comportamiento de alguien, afectando incluso decisiones de compra.&nbsp;</p>

                        <p style="text-align:center">Esta relaci&oacute;n surge de la confianza y credibilidad que la persona tiene con sus seguidores, con su p&uacute;blico, la cual se crea con el paso del tiempo y est&aacute; sustentada en la comunicaci&oacute;n y la experiencia.&nbsp;&nbsp;</p>

                        <p style="text-align:center">Normalmente, cuando deseamos comprar/adquirir un producto nuevo, diferente o poco conocido, se tiende a buscar informaci&oacute;n adicional para verificar su calidad. Es ah&iacute; cuando se recurre a internet y/o a la experiencia de personas cercanas que ya han tenido un acercamiento con el producto.&nbsp;&nbsp;</p>

                        <p style="text-align:center">Buscamos la opini&oacute;n de familia, amigos, conocidos e incluso desconocidos, lo importante en este &uacute;ltimo caso es que tenga una s&oacute;lida reputaci&oacute;n. S&iacute;, cuando hablamos de esos desconocidos con s&oacute;lida reputaci&oacute;n, nos referimos a los influencers; a los Instagramers, Tiktokers, Youtubers, Twitteros y Bloggeros.&nbsp;</p>

                        <p style="text-align:center">Hay casos en los que los influencers le dan a la marca una cara humana que ayuda a la conexi&oacute;n con el p&uacute;blico y en otros momentos, ellos son las personas quienes siembran en su p&uacute;blico el deseo o necesidad de comprar/adquirir un producto a trav&eacute;s de su recomendaci&oacute;n directa o indirecta.&nbsp;&nbsp;</p>

                        <p style="text-align:center">Lo que aporta un influencer a una marca y su impacto es variable seg&uacute;n su naturaleza. Ellos pueden ser especialistas en el tema, sus rese&ntilde;as o comentarios son reconocidos nacional y/o internacionalmente, tienen un movimiento de seguidores significativo, con alta interacci&oacute;n por su estilo de vida. El punto es encontrar esa conexi&oacute;n entre tu marca y lo que el/la influencer crea, produce y apoya para tener resultados exitosos.&nbsp;</p>

                        <p style="text-align:center">Ahora que sabes qu&eacute; es y por qu&eacute; tu marca necesita un influencer, te invitamos a dar el siguiente paso con nuestra gu&iacute;a para seleccionar el influencer perfecto, donde te ayudaremos a saber qu&eacute; quieres que logre tu influencer, los factores a tener en cuenta para que cumpla el objetivo y finalmente escoger al que mejor se ajuste para tus necesidades.&nbsp;</p>

